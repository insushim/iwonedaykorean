export function getPassageById(id: string): Passage | undefined {
  return ALL_PASSAGES.find((p) => p.id === id);
}

export function getPassagesByGrade(grade: number, semester?: 1 | 2): Passage[] {
  if (semester) {
    return ALL_PASSAGES.filter((p) => p.grade === grade && p.semester === semester);
  }
  return ALL_PASSAGES.filter((p) => p.grade === grade);
}

export function getPassagesByGradeGroup(gradeGroup: '1-2' | '3-4' | '5-6'): Passage[] {
  return ALL_PASSAGES.filter((p) => p.gradeGroup === gradeGroup);
}

