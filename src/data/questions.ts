export const ALL_QUESTIONS: Question[] = [
  ...SEED_QUESTIONS,
  ...GRAMMAR_QUESTIONS,
  ...ADDITIONAL_PASSAGE_QUESTIONS,
];

// -----------------------------------------------------------------------------
// Helper Functions
// -----------------------------------------------------------------------------

export function getQuestionsByPassageId(passageId: string): Question[] {
  return ALL_QUESTIONS.filter((q) => q.passageId === passageId);
}

export function getQuestionById(id: string): Question | undefined {
  return ALL_QUESTIONS.find((q) => q.id === id);
}

export function getGrammarQuestionsByGradeSemester(
  grade: number,
  semester: 1 | 2
): Question[] {
  const prefix = `G-${grade}-${semester}`;
  return GRAMMAR_QUESTIONS.filter((q) => q.id.startsWith(prefix));
}

export function getQuestionsByDifficulty(difficulty: 'easy' | 'medium' | 'hard'): Question[] {
  return ALL_QUESTIONS.filter((q) => q.difficulty === difficulty);
}

export function getQuestionsByCategory(category: string): Question[] {
  return ALL_QUESTIONS.filter((q) => q.category === category);
}

export function getGrammarQuestions(): Question[] {
  return GRAMMAR_QUESTIONS;
}
