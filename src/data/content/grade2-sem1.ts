// Questions helper
function makeQ(
  passage: Passage,
  qs: [
    string,
    string,
    string,
    string,
    string,
    1 | 2 | 3 | 4,
    string,
    Record<number, string>,
    string,
    "easy" | "medium" | "hard",
    string,
  ][],
): PassageWithQuestions {
  return {
    passage,
    questions: qs.map(
      ([question, c1, c2, c3, c4, ca, exp, we, std, diff, cat], i) =>
        q(
          `${passage.id}-Q${i + 1}`,
          passage.id,
          i + 1,
          question,
          c1,
          c2,
          c3,
          c4,
          ca,
          exp,
          we,
          std,
          diff,
          cat,
        ),
    ),
  };
}

