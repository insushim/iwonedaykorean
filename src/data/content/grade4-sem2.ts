import type { Passage, Question } from "@/types";
interface PassageWithQuestions {
  passage: Passage;
  questions: Question[];
}

function p(
  id: string,
  type: "nonfiction" | "fiction" | "poetry",
  title: string,
  content: string,
  std: string[],
  kw: string[],
  diff: "easy" | "medium" | "hard" = "easy",
): Passage {
  return {
    id,
    type,
    gradeGroup: "3-4",
    grade: 4 as any,
    semester: 2 as any,
    title,
    content,
    relatedStandards: std,
    keywords: kw,
    wordCount: content.length,
    difficulty: diff,
  };
}
function q(
  id: string,
  pid: string,
  qn: number,
  question: string,
  c1: string,
  c2: string,
  c3: string,
  c4: string,
  ca: 1 | 2 | 3 | 4,
  exp: string,
  we: Record<number, string>,
  std: string,
  diff: "easy" | "medium" | "hard",
  cat: string,
): Question {
  return {
    id,
    passageId: pid,
    questionNumber: qn,
    type: "multiple_choice",
    question,
    choices: [
      { number: 1, text: c1 },
      { number: 2, text: c2 },
      { number: 3, text: c3 },
      { number: 4, text: c4 },
    ],
    correctAnswer: ca,
    explanation: exp,
    wrongExplanations: we,
    relatedStandard: std,
    difficulty: diff,
    category: cat,
  };
}

